document.getElementById('menu-button').addEventListener('click', toggleThem)

function toggleThem() {
    if (document.getElementById('menu').classList.contains('is-hidden')) { // If the menu is hidden...
        document.getElementById('menu').classList.remove('is-hidden') // ...show the menu and...
        document.querySelector('.icon-hamburger').classList.add('is-hidden') // ...hide the hamburger and...
        document.querySelector('.icon-close-menu').classList.remove('is-hidden') // ...show the X.
        document.querySelector('.icon-close-menu').style.width = '23px'
        document.querySelector('.icon-close-menu').style.height = '23px'
    } else { // Otherwise...
        document.getElementById('menu').classList.add('is-hidden') // ...hide the menu and...
        document.querySelector('.icon-hamburger').classList.remove('is-hidden') // ...show the hamburger and...
        document.querySelector('.icon-close-menu').classList.add('is-hidden') // ...hide the X.
        document.querySelector('.icon-hamburger').style.width = '29px'
        document.querySelector('.icon-hamburger').style.height = '19.3px'
    } 
}